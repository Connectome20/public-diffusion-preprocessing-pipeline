def execute_commands_with_env(commands, env_exports, success_message, subject_id=None):
    # Append commands to script file if subject_id is provided
    if subject_id:
        base_dir=f"/your/project/directory/bids/sub-{subject_id}/dwi_process"
        subject = f"sub-{subject_id}"
        # Append environment setup
        append_command_to_script(env_exports.strip(), base_dir, subject)
        # Append each command
        for cmd in commands:
            append_command_to_script(cmd, base_dir, subject)
    else:
        # Try to get subject ID from current context
        subject_ids = get_subject_ids()
        if subject_ids:
            for sid in subject_ids:
                base_dir = f"/your/project/directory/bids/sub-{sid}/dwi_process"
                subject = f"sub-{sid}"
                append_command_to_script(env_exports.strip(), base_dir, subject)
                for cmd in commands:
                    append_command_to_script(cmd, base_dir, subject)
    
    # Create a temporary shell script file
    with tempfile.NamedTemporaryFile(mode='w', suffix='.sh', delete=False) as temp_script:
        script_path = temp_script.name
        # Write the environment exports and commands to the script
        temp_script.write('#!/bin/bash\n')
        temp_script.write(env_exports + '\n')
        for cmd in commands:
            temp_script.write(cmd + '\n')

    # Make the script executable
    os.chmod(script_path, 0o775)

    # Execute the script
    status_text.insert('end', f"Executing Script: {script_path}\n")
    status_text.see('end')

    try:
        completed_process = subprocess.run(
            f"/bin/bash {script_path}", shell=True, check=True,
            stdout=subprocess.PIPE, stderr=subprocess.PIPE
        )

        stdout = completed_process.stdout.decode('latin-1', errors='replace')
        stderr = completed_process.stderr.decode('latin-1', errors='replace')


        if stdout:
            status_text.insert('end', f"{success_message}\nOutput:\n{stdout}\n")
        if stderr:
            status_text.insert('end', f"Standard Error:\n{stderr}\n")

        return True

    except subprocess.CalledProcessError as e:
        error_output = e.stderr or e.stdout
        status_text.insert('end', f"An error occurred:\n{error_output}\n")
        return False

    finally:
        status_text.see('end')
        # Clean up the temporary script file
        os.remove(script_path)

