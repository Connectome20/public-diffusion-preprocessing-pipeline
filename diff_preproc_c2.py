def execute_all_steps():
    """
    Sequentially executes all the processing steps, waiting for each to complete before proceeding.
    """
    def check_dwi_file_entries():
        """Check if any of the DWI file entries contains a valid path."""
        for entry in dwi_file_entries:
            if entry.get("1.0", 'end-1c').strip():  # Check if any entry is not empty
                return True
        return False
    
    def run_step(step_function, next_steps):
        try:
            step_function()  # Execute the step
            if next_steps:  # If there are more steps, proceed with the next one
                root.after(100, run_step, next_steps[0], next_steps[1:])
            else:
                msgbox.showinfo("Success", "All selected steps executed successfully.")
        except Exception as e:
            msgbox.showerror("Error", f"An error occurred during processing: {str(e)}")

    # Determine which concatenate DWI step to include
    if check_dwi_file_entries():
        concatenate_step = concatenate_dwi_data_from_gui_entries
    else:
        concatenate_step = execute_concatenate_dwi_data
    
    # Build the steps list dynamically based on checkbox selections
    steps = []
    
    if step_dcm2bids.get():
        steps.append(execute_dcm2bids)
    
    if step_export_params.get():
        steps.append(execute_export_diffusion_parameters)
    
    if step_concatenate.get():
        steps.append(concatenate_step)  # Use the chosen concatenate function
    
    # Add denoise step if checkbox is selected AND denoise option is not "no_denoise"
    denoise_option_value = denoise_option.get()
    selected_flow_value = selected_flow.get()
    
    if step_denoise.get() and (selected_flow_value == "concat_denoise_degibbs" or denoise_option_value != "no_denoise"):
        steps.append(execute_denoise)
    
    # Continue with remaining steps based on checkboxes
    if step_degibbs.get():
        steps.append(execute_degibbs)
    
    if step_topup.get():
        steps.append(execute_topup)
    
    if step_masks.get():
        steps.append(execute_generate_masks)
    
    if step_eddy.get():
        steps.append(execute_eddy)
    
    if step_gnc.get():
        steps.append(execute_gnc_dwi)
    
    if step_interpolation.get():
        steps.append(execute_interpolation_eddy_gnc)
    
    if step_MPPCA_noise.get():
        steps.append(MPPCA_noise)
    
    if step_write_output.get():
        steps.append(write_output_to_processed_dwi)

    if step_gnc_anat.get():
        steps.append(execute_gnc_anat)
    
    if step_recon_all.get():
        steps.append(execute_recon_all)
    
    if step_sandi_division.get():
        steps.append(execute_sandi_volume_division)
    
    if step_tractcaliber.get():
        steps.append(execute_tractcaliber_volume_division)
    
    if step_tractseg.get():
        steps.append(run_tractseg_for_all_subjects)
    
    if steps:
        run_step(steps[0], steps[1:])
    else:
        msgbox.showwarning("No steps selected", "Please select at least one processing step.")


