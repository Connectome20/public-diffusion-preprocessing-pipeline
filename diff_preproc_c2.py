def append_command_to_script(command, base_dir, subject):
    script_dir = f"{base_dir}/s0_script"
    os.makedirs(script_dir, exist_ok=True)
    script_path = f"{script_dir}/{subject}_script_to_run.sh"
    
    # Check if file exists, if not create with shebang
    if not os.path.exists(script_path):
        with open(script_path, 'w') as file:
            file.write('#!/bin/bash\n\n')
            file.write('# Script generated by DWI preprocessing GUI\n')
            file.write(f'# Subject: {subject}\n')
            file.write(f'# Date: {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}\n\n')
        # Make the script executable
        os.chmod(script_path, 0o755)
    
    with open(script_path, 'a') as file:
        # Add timestamp comment
        file.write(f'\n# Command added at {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}\n')
        
        # Handle different command formats
        if 'export' in command and '\n' in command and not '&&' in command:
            # Write environment exports as separate lines
            lines = command.strip().split('\n')
            for line in lines:
                if line.strip():
                    file.write(line.strip() + '\n')
        elif '\\' in command and '&&' in command:
            # Handle multi-line commands with backslashes (like TractSeg)
            # These are already properly formatted with line continuations
            file.write(command.strip() + '\n')
        elif '&&' in command and not '\\' in command:
            # Format multi-command lines nicely
            parts = command.split('&&')
            for i, part in enumerate(parts):
                if i == 0:
                    file.write(part.strip())
                else:
                    file.write(' && \\\n    ' + part.strip())
            file.write('\n')
        else:
            # Single line command - clean up excessive whitespace
            cleaned_command = ' '.join(command.split())
            file.write(cleaned_command + '\n')
    
    #msgbox.showinfo("Success", f"Command appended successfully to {script_path}")

