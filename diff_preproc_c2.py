def run_tractseg_for_subject(subject_id):
    """
    Executes the TractSeg pipeline steps directly in Python for a specific subject.
    """
    base_dir = bidspath_entry.get("1.0", "end-1c").strip()  # Dynamically get the base path from the entry
    dwi_dir = f"{base_dir}/sub-{subject_id}/dwi_process/s13_SANDI"
    output_dir = f"/your/project/directory/bids/derivatives/TractSeg_v1/sub-{subject_id}/"

    dwi_nii = f"{dwi_dir}/difftimeb_ms.nii.gz"
    bval_txt = f"{dwi_dir}/bvals_difftimeb_ms.txt"
    bvec_txt = f"{dwi_dir}/bvecs_difftimeb_ms.txt"
    mask_nii = f"/your/project/directory/bids/derivatives/processed_dwi/sub-{subject_id}/sub-{subject_id}_brain_mask.nii.gz"

    
    try:
        # Step 1: Generate PEAKS and binary tract segmentation maps
        tractseg_command = f"""
        {env_activate_command} && \
        TractSeg -i {dwi_nii} --brain_mask {mask_nii} -o {tractseg_out_dir} \
        --raw_diffusion_input --csd_type csd_msmt --bvals {bval_txt} --bvecs {bvec_txt} \
        --keep_intermediate_files --output_type tract_segmentation
        """
        # Append command to script
        base_dir_dwi = f"{base_dir}/sub-{subject_id}/dwi_process"
        subject = f"sub-{subject_id}"
        append_command_to_script(tractseg_command.strip(), base_dir_dwi, subject)
        subprocess.run(tractseg_command, shell=True, check=True)

        # Step 2: Generate tract end ROIs
        tract_endings_command = f"""
        {env_activate_command} && \
        TractSeg -i {tractseg_out_dir}peaks.nii.gz -o {tractseg_out_dir} --output_type endings_segmentation
        """
        append_command_to_script(tract_endings_command.strip(), base_dir_dwi, subject)
        subprocess.run(tract_endings_command, shell=True, check=True)

        # Step 3: Generate tract-orientation maps (TOM)
        tom_command = f"""
        {env_activate_command} && \
        TractSeg -i {tractseg_out_dir}peaks.nii.gz -o {tractseg_out_dir} --output_type TOM
        """
        append_command_to_script(tom_command.strip(), base_dir_dwi, subject)
        subprocess.run(tom_command, shell=True, check=True)

        # Step 4: Perform tractography within tract-masks
        tracking_command = f"""
        {env_activate_command} && \
        Tracking -i {tractseg_out_dir}peaks.nii.gz -o {tractseg_out_dir} --tracking_format tck --nr_fibers 2000
        """
        append_command_to_script(tracking_command.strip(), base_dir_dwi, subject)
        subprocess.run(tracking_command, shell=True, check=True)

        status_text.insert('end', f"TractSeg processing for {subject_id} completed successfully.\n")

    except subprocess.CalledProcessError as e:
        status_text.insert('end', f"An error occurred while running TractSeg for {subject_id}: {e.stderr}\n")
    finally:
        status_text.see('end')  # Auto-scroll to the end


