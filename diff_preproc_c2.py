def run_mrconvert(input_file, bvec_file, bval_file, indices, output_file, output_bvec_file, output_bval_file, subject_id, folder_name):
    """
    Runs the mrconvert command to process DWI volumes based on the specified indices.
    """
    base_dir = bidspath_entry.get("1.0", "end-1c").strip()  # Dynamically get the base path from the BIDS path entry
    output_dir = f"{base_dir}/{subject_id}/dwi_process/{folder_name}"
    os.makedirs(output_dir, exist_ok=True)

    full_output_file = f"{output_dir}/{output_file}"
    full_output_bvec_file = f"{output_dir}/{output_bvec_file}"
    full_output_bval_file = f"{output_dir}/{output_bval_file}"

    if indices:
        index_range = ",".join(map(str, indices))  # Use specific indices instead of a range
    else:
        status_text.insert('end', f"No indices provided for {subject_id}.\n")
        return

    command = [
        "mrconvert", input_file,
        "-fslgrad", bvec_file, bval_file,
        "-coord", "3", index_range,
        full_output_file,
        "-export_grad_fsl", full_output_bvec_file, full_output_bval_file,
        "-force"
    ]
    
    # Convert command list to string
    cmd_str = " ".join(command)
    
    # Append command to script
    base_dir_dwi = f"{base_dir}/{subject_id}/dwi_process"
    subject = subject_id
    append_command_to_script(cmd_str, base_dir_dwi, subject)
    
    try:
        # Execute the mrconvert command
        subprocess.run(command, check=True)
        status_text.insert('end', f"mrconvert executed successfully for {subject_id}. Output saved to {output_dir}.\n")
    except subprocess.CalledProcessError as e:
        status_text.insert('end', f"Error running mrconvert for {subject_id}: {e.stderr}\n")
    status_text.see('end')

